/**
 * Portfolio Scanner
 * Run this script after adding new photos to Assests/Portfolio/
 * It auto-generates portfolio-data.js with all image files found.
 *
 * Usage: node update-portfolio.js
 */

const fs = require('fs');
const path = require('path');

const portfolioDir = path.join(__dirname, 'Assests', 'Portfolio');
const outputFile = path.join(__dirname, 'portfolio-data.js');

// Supported image extensions
const imageExts = new Set(['.jpg', '.jpeg', '.png', '.webp', '.gif', '.bmp']);

function scanPortfolio() {
  if (!fs.existsSync(portfolioDir)) {
    console.error('Portfolio directory not found:', portfolioDir);
    process.exit(1);
  }

  const files = fs.readdirSync(portfolioDir)
    .filter(file => imageExts.has(path.extname(file).toLowerCase()))
    .sort((a, b) => a.localeCompare(b, undefined, { numeric: true, sensitivity: 'base' }));

  const images = files.map((file, i) => ({
    src: `Assests/Portfolio/${file}`,
    title: `Project ${i + 1}`
  }));

  const jsContent = `// Auto-generated by update-portfolio.js
// Do NOT edit manually. Run: node update-portfolio.js
window.portfolioData = ${JSON.stringify(images, null, 2)};
`;

  fs.writeFileSync(outputFile, jsContent, 'utf8');
  console.log(`✓ Scanned ${images.length} images from Portfolio folder`);
  console.log(`✓ Generated ${path.basename(outputFile)}`);
}

scanPortfolio();
